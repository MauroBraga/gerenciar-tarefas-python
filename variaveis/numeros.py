# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# subtracao -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# multiplicacao *
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# divisao /
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))

print("Valor em inteiro=", int(divisao))

# float()
print("Valor em float = ", float(soma))

# Modulo %
modulo = 5 % 2
print("Modulo = ", modulo)

# divisao inteiro //
divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))